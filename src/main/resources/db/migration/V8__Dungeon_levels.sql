DROP TABLE IF EXISTS `dungeon_levels`;
CREATE TABLE `dungeon_levels` (
	`id` BIGINT NOT NULL AUTO_INCREMENT,
	`dungeon_key` VARCHAR(127) NOT NULL,
	`level` INT NOT NULL,
	`total_exp_required` DOUBLE NOT NULL,
	`updated_at` TIMESTAMP NOT NULL DEFAULT current_timestamp(),

	PRIMARY KEY (`id`),
	UNIQUE KEY `dungeon_level` (`dungeon_key`, `level`),
	CONSTRAINT `dungeon_levels_ibfk_1` FOREIGN KEY (`dungeon_key`) REFERENCES `dungeons` (`key`)
);

